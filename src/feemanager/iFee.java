             String receiptString = "";
             for(int i=0;i <feetype.length; i++){
                 receiptString += feetype[i]+"\t\t"+amountPaid[i]+"\n";
                 System.out.println(receiptString);
             }
          
             
             receipt.append("\t\nRECEIPT:\n\n"+
                     "\t"+receiptString+ "\t"
                     +"Total "+total
                     
                     
             );
