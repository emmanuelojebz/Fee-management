    private void feesTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_feesTableMouseClicked
        // TODO add your handling code here:
        dataLoaded = false;
        try {
            int row = feesTable.getSelectedRow();
            String rowId = (feesTable.getModel().getValueAt(row,0).toString());
            stm = conn.createStatement();
            sqlResult = stm.executeQuery("SELECT * FROM fee WHERE ReceiptNo="+rowId );
            if(sqlResult.next()){
                String receiptId = sqlResult.getString("ReceiptNo");
                receipt_no.setText(receiptId);
                
                String studentId = sqlResult.getString("StudentId");
                student_id.setText(studentId);
                
                String studentName = sqlResult.getString("StudentName");
                student_name.setText(studentName);

                String studentLevel = sqlResult.getString("StudentLevel");
                student_level.setText(studentLevel);

                String paymentType  = sqlResult.getString("PaymentType");
                ptype_cb.setSelectedItem(paymentType);

                String feeType = sqlResult.getString("FeeType");
               
                ftype_cb.setSelectedItem(feeType);
                
                String amountPaid = sqlResult.getString("AmountPaid");
                amount_paid.setText(amountPaid);
                
                String paymentDate = sqlResult.getString("PaymentDate");
                date_chooser.setDate(datef.parse(paymentDate));

            }
        }
        catch(Exception e){
            
        }
        
    }//GEN-LAST:event_feesTableMouseClicked

