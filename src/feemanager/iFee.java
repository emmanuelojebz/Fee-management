    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
                        String sql = "DELETE FROM fee WHERE ReceiptNo=?";
            PreparedStatement Pstm = conn.prepareStatement(sql);
            Pstm.setString(1,receipt_no.getText());
            int query = JOptionPane.showConfirmDialog(null, "Are you sure you want to Delete this Record ?");
            if(query == JOptionPane.YES_OPTION ){
                Pstm.execute();
                JOptionPane.showMessageDialog(null,"Record Deleted");
                receipt_no.setText("");
                student_id.setText("");
                student_name.setText("");
                student_level.setText("");
                amount_paid.setText("");
                date_chooser.setDate(new Date());
            }

        }
        catch(SQLException |HeadlessException e){
            JOptionPane.showMessageDialog(null, e);
        }
        GetData();
 
    }//GEN-LAST:event_jButton1ActionPerformed

